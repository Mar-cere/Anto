export function pickRandom(arr, fallback = '') {
  if (!Array.isArray(arr) || arr.length === 0) return fallback;
  const v = arr[Math.floor(Math.random() * arr.length)];
  if (v === undefined || v === null) return fallback;
  return v;
}

